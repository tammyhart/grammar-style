#!/usr/bin/env node
import fs from "node:fs/promises"
import path from "node:path"
import { createRequire } from "node:module"
import { fileURLToPath } from "node:url"
import { loadConfig, createTheme } from "../dist/index.mjs"

const require = createRequire(import.meta.url)

async function run() {
  const args = process.argv.slice(2)
  const command = args[0]

  if (command !== "generate") {
    console.error("Unknown command. Did you mean 'generate'?")
    process.exit(1)
  }

  try {
    const cwd = process.cwd()
    const config = await loadConfig(cwd)

    if (!config) {
      console.error("❌ Could not find grammar.config file.")
      process.exit(1)
    }

    const theme = createTheme(config)
    const { media, breakpoint } = theme

    // Resolve the current installation of grammar-style by looking relative to this script
    const currentScriptDir = path.dirname(fileURLToPath(import.meta.url))
    const grammarStyleDir = path.dirname(currentScriptDir)

    const generatedCode = `// THIS FILE IS AUTO-GENERATED BY GRAMMAR-STYLE
// DO NOT EDIT MANUALLY.
export const media = ${JSON.stringify(media, null, 2)};
export const breakpoint = ${JSON.stringify(breakpoint, null, 2)};
export const hasGenerated = true;
`

    const generatedMjsCode = generatedCode;
    const generatedCjsCode = `// THIS FILE IS AUTO-GENERATED BY GRAMMAR-STYLE
// DO NOT EDIT MANUALLY.
exports.media = ${JSON.stringify(media, null, 2)};
exports.breakpoint = ${JSON.stringify(breakpoint, null, 2)};
exports.hasGenerated = true;
`

    // Output to the dist folder inside the node_modules location
    const distMjsPath = path.join(grammarStyleDir, "dist", "generated.mjs")
    const distCjsPath = path.join(grammarStyleDir, "dist", "generated.js")

    // We only write to where they already exist to be safe, or just force write
    await fs.writeFile(distMjsPath, generatedMjsCode, "utf8").catch(() => {})
    await fs.writeFile(distCjsPath, generatedCjsCode, "utf8").catch(() => {})

    console.log("✅ Successfully generated global context!")
    console.log("✨ You can now safely 'import { media } from \"grammar-style\"' in zero-runtime CSS components.")
  } catch (error) {
    console.error("❌ Failed to generate configuration", error)
    process.exit(1)
  }
}

run()
